var lang = {};
lang['da_DK'] = langDk;
lang['en_GB'] = langEn;

function interpretError(err, locale) {
	var l = lang[locale];	
	if (!l) {
		l = lang['en_GB'];
	}
	
	var result = {};
	result["headline"] = l[err.code + "_headline"];
	result["message"] = l[err.code + "_message"];
	
	if (!result.headline) {
		result["headline"] = l["generel_headline"];
		result["message"] = l["generel_message"];
	}
	
	return result;
}